#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void		*p;
	long int	d;

	if (!count || !size)
	{
		count = 1;
		size = 1;
	}
	d = count * size;
	if (d > INT32_MAX)
		return (NULL);
	p = malloc(count * size);
	if (!p)
		return (NULL);
	ft_bzero(p, count * size);
	return (p);
}
